def make_entry(author_id, display_name, aliases, birth_year, death_year, tradition, nationality, works, notes):
    return {
        "author_id": author_id,
        "display_name": display_name,
        "aliases": aliases,
        "birth_year": birth_year,
        "death_year": death_year,
        "tradition": tradition,
        "nationality": nationality,
        "works": works,
        "notes": notes,
    }


